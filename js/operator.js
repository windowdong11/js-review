// numeric, 산술
console.log(1 + 2)
console.log(1 - 2)
console.log(1 * 2)
console.log(1 / 2)
console.log(1 % 2)

// assignment, 대입
let a = 1
console.log(a)
a = 2
console.log(a)
a += 1
console.log(a)
a -= 1
console.log(a)
// ... etc

// comparision operator
// 타입 자동 변환하여 비교
console.log("1" == 1.0)
console.log("1" != 1.0)
// 타입 변환하지 않고 비교
console.log("1" === 1.0)
console.log("1" !== 1.0)
console.log(1 > 2)
console.log(1 < 2)
console.log(1 >= 2)
console.log(1 <= 2)

// logical, 논리
console.log(true && false)
console.log(true || false)
console.log(!true)